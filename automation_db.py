def main():
    init_db()
    sunrise, sunset = get_sun_times()
    last_sun_fetch_day = None
    door_open_time = sunrise - timedelta(minutes=DOOR_OPEN_OFFSET_MIN)
    door_close_time = sunset + timedelta(minutes=DOOR_CLOSE_OFFSET_MIN)
    print(f"[TIME] sunrise={sunrise.strftime('%H:%M')} sunset={sunset.strftime('%H:%M')}")
    try:
        while True:
            now = datetime.now(LOCAL_TZ)
            if last_sun_fetch_day != now.date():
                sunrise, sunset = get_sun_times()
                door_open_time = sunrise - timedelta(minutes=DOOR_OPEN_OFFSET_MIN)
                door_close_time = sunset + timedelta(minutes=DOOR_CLOSE_OFFSET_MIN)
                last_sun_fetch_day = now.date()
            row = fetch_control()
            apply_door_state(row, door_open_time, door_close_time)
            apply_feeder_state(row, sunrise, sunset)
            target_fan = compute_fan_target(row)
            set_fan_pct(target_fan)
            is_dark = now >= door_close_time or now < door_open_time
            if is_dark:
                predator_led.on()
            else:
                predator_led.off()
            print(f"[STATE] door={row['door_status']} feeder={row['feeder_status']} fan={target_fan}% auto_door={row['door_auto']} auto_feeder={row['feeder_auto']} auto_fan={row['fan_auto']}")
            time.sleep(POLL_SECONDS)
    except KeyboardInterrupt:
        print("[SYS] stoppen")
    finally:
        stop_door()
        stop_feeder()
        fan_off()
        predator_led.off()
        print("[SYS] GPIO veilig afgesloten")

