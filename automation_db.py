def set_fan_pct(pct):
    try:
        pct = max(0, min(100, float(pct)))
    except Exception:
        pct = 0

    if pct <= 0:
        fan_off()
    else:
        fan_on()

    update_status(fan_status_pct=pct)


def compute_fan_target(row):
    if row["fan_auto"]:
        return row["fan_speed_pct"] if row["fan_speed_pct"] is not None else 0
    return row["fan_override_pct"] if row["fan_override_pct"] is not None else 0




def compute_door_target(row, door_open_time, door_close_time) -> str:
    now = datetime.now(LOCAL_TZ)

    if row["door_auto"]:
        if now >= door_close_time:
            return "closed"
        elif now >= door_open_time:
            return "open"
        else:
            return "closed"

    return row["door_target"]


def compute_feeder_target(row, feeder_open_time, feeder_close_time) -> str:
    now = datetime.now(LOCAL_TZ)

    if row["feeder_auto"]:
        if feeder_open_time <= now < feeder_close_time:
            return "open"
        return "closed"

    return row["feeder_target"]





def apply_door_state(row, door_open_time, door_close_time):
    desired = compute_door_target(row, door_open_time, door_close_time)

    if row["door_auto"] and row["door_target"] != desired:
        update_status(door_target=desired)

    status = row["door_status"]

    if status == "error":
        print("[DOOR] status is error, geen automatische actie")
        return

    if desired == status:
        return

    if desired == "open":
        open_door()
    elif desired == "closed":
        close_door()


def apply_feeder_state(row, feeder_open_time, feeder_close_time):
    desired = compute_feeder_target(row, feeder_open_time, feeder_close_time)

    if row["feeder_auto"] and row["feeder_target"] != desired:
        update_status(feeder_target=desired)

    status = row["feeder_status"]

    if status == "error":
        print("[FEEDER] status is error, geen automatische actie")
        return

    if desired == status:
        return

    if desired == "open":
        feeder_open()
    elif desired == "closed":
        feeder_close()

"""##Main loop automation (met verbeterde code)"""

def main_loop():
    print("[MAIN] start automation loop")

    while True:
        try:
            sync_door_status_from_switches()

            row = fetch_control()
            if not validate_row(row):
                time.sleep(POLL_SECONDS)
                continue

            sunrise, sunset = get_sun_times()

            door_open_time = sunrise + timedelta(minutes=DOOR_OPEN_OFFSET_MIN)
            door_close_time = sunset + timedelta(minutes=DOOR_CLOSE_OFFSET_MIN)

            feeder_open_time = sunrise + timedelta(minutes=FEEDER_OPEN_OFFSET_MIN)
            feeder_close_time = sunset + timedelta(minutes=FEEDER_CLOSE_OFFSET_MIN)

            apply_door_state(row, door_open_time, door_close_time)
            apply_feeder_state(row, feeder_open_time, feeder_close_time)

            fan_target = compute_fan_target(row)
            set_fan_pct(fan_target)

        except Exception as e:
            print(f"[MAIN] onverwachte fout: {e}")

        time.sleep(POLL_SECONDS)

"""##Final loop automation"""

def run():
    try:
        setup()
        main_loop()
    except KeyboardInterrupt:
        print("[MAIN] handmatig gestopt")
    except Exception as e:
        print(f"[MAIN] error: {e}")
        raise
    finally:
        cleanup()

if __name__ == "__main__":
    run()

