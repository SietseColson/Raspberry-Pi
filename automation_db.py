def fetch_control() -> Optional[dict]:
    conn = get_db_connection()
    try:
        with conn.cursor() as cur:
            cur.execute("SELECT * FROM device_control WHERE id = 1")
            row = cur.fetchone()
            if row is None:
                return None
            cols = [d.name for d in cur.description]
            return dict(zip(cols, row))
    finally:
        release_db_connection(conn)


def update_status(**kwargs) -> bool:
    if not kwargs:
        return False

    invalid = [k for k in kwargs if k not in VALID_UPDATE_COLUMNS]
    if invalid:
        print(f"[DB] Ongeldige kolommen in update_status: {invalid}")
        return False

    cols = ", ".join([f"{k} = %s" for k in kwargs])
    vals = list(kwargs.values())
    vals.append(1)

    conn = get_db_connection()
    try:
        with conn.cursor() as cur:
            cur.execute(
                f"UPDATE device_control SET {cols}, updated_at = CURRENT_TIMESTAMP WHERE id = %s",
                vals
            )
        conn.commit()
        return True
    except Exception as e:
        conn.rollback()
        print(f"[DB] Update fout: {e}")
        return False
    finally:
        release_db_connection(conn)


def _fixed_backup_sun_times() -> Tuple[datetime, datetime]:
    now = datetime.now(LOCAL_TZ)
    sunrise = now.replace(
        hour=BACKUP_SUNRISE_HOUR,
        minute=BACKUP_SUNRISE_MINUTE,
        second=0,
        microsecond=0
    )
    sunset = now.replace(
        hour=BACKUP_SUNSET_HOUR,
        minute=BACKUP_SUNSET_MINUTE,
        second=0,
        microsecond=0
    )
    return sunrise, sunset


def get_sun_times() -> Tuple[datetime, datetime]:
    try:
        url = f"https://api.sunrise-sunset.org/json?lat={LATITUDE}&lng={LONGITUDE}&formatted=0"
        response = requests.get(url, timeout=10)
        response.raise_for_status()

        payload = response.json()
        results = payload["results"]

        sunrise = datetime.fromisoformat(results["sunrise"].replace("Z", "+00:00")).astimezone(LOCAL_TZ)
        sunset = datetime.fromisoformat(results["sunset"].replace("Z", "+00:00")).astimezone(LOCAL_TZ)

        LAST_VALID_SUN_TIMES["sunrise"] = sunrise
        LAST_VALID_SUN_TIMES["sunset"] = sunset

        return sunrise, sunset

    except Exception as e:
        print(f"[SUN] API fout: {e}")

        if LAST_VALID_SUN_TIMES["sunrise"] and LAST_VALID_SUN_TIMES["sunset"]:
            print("[SUN] Gebruik laatst geldige sunrise/sunset")
            return LAST_VALID_SUN_TIMES["sunrise"], LAST_VALID_SUN_TIMES["sunset"]

        print("[SUN] Gebruik vaste backup sunrise/sunset")
        return _fixed_backup_sun_times()




